num = int(input("Введите четырёхзначное число: "))

if 1000 <= num <= 9999:
    d1 = num // 1000  
    d2 = (num // 100) % 10  
    d3 = (num // 10) % 10  
    d4 = num % 10  
    
    reversed_num = d4 * 1000 + d3 * 100 + d2 * 10 + d1
    print("Перевёрнутое число:", reversed_num)
else:
    print("Ошибка! Введите четырёхзначное число.")